package pl.edu.uwr.pum.flagi;

public class Rekord {

    private String country;
    private byte[] flag;
    private Integer indeks;
    private String podpowiedz1;
    private String podpowiedz2;


    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public byte[] getFlag() {
        return flag;
    }

    public void setFlag(byte[] flag) {
        this.flag = flag;
    }

    public Integer getIndeks() {
        return indeks;
    }

    public void setIndeks(Integer indeks) {
        this.indeks = indeks;
    }

    public String getPodpowiedz1() {
        return podpowiedz1;
    }

    public void setPodpowiedz1(String podpowiedz1) {
        this.podpowiedz1 = podpowiedz1;
    }

    public String getPodpowiedz2() {
        return podpowiedz2;
    }

    public void setPodpowiedz2(String podpowiedz2) {
        this.podpowiedz2 =podpowiedz2;
    }


}
